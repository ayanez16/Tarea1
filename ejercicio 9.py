#Solicitar un número entre el 1 y el 12 e imprimir el mes correspondiente a dicho
#número.
numero = int(input("Ingrese un numero del 1 al 12: "))
if numero == 1:
    print("Enero")
elif numero == 2:
    print("Febrero")
elif numero == 3:
    print("Marzo")
elif numero == 4:
    print("Abril")
elif numero == 5:
    print("Mayo")
elif numero == 6:
    print("Junio")
elif numero == 7:
    print("Julio")
elif numero == 8:
    print("Agosto")
elif numero == 9:
    print("Septiembre")
elif numero == 10:
    print("Octubre")
elif numero == 11:
    print("Noviembre")
elif numero == 12:
    print("Diciembre")
else:
    print("Por favor ingresar un numero correcto")